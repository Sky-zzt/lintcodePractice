def swap(arr, start, end):
    temp = arr[start]
    arr[start] = arr[end]
    arr[end] = temp


def partition(arr, left, right, target):
    less = left - 1
    more = right + 1
    while left < more:
        if arr[left] < target:
            less += 1
            swap(arr, left, less)
            left += 1
        elif arr[left] > target:
            more -= 1
            swap(arr, left, more)
        else:
            left += 1
    return less + 1, more - 1

import random
def quicksort(arr, left, right):
    if left < right:
        p = partition(arr, left, right, arr[left+int(random.random()*right-left+1)])
        quicksort(arr, left, p[0]-1)
        quicksort(arr, p[1]+1, right)


def sort(arr):
    return quicksort(arr, 0, len(arr) - 1)



